import { AccessControlAction } from 'app/types/accessControl';

import { notificationsPermissions } from '../../../utils/access-control';
import { isProvisionedResource } from '../../../utils/k8s/utils';
import { isAdmin } from '../../../utils/misc';
import { makeAbility } from '../abilityUtils';
import { type Ability, Granted, InsufficientPermissions, NotificationPolicyAction, Provisioned } from '../types';

/** Minimal shape needed to check provenance on a notification policy/route. */
export type NotificationPolicyContext = { provenance?: string };

export type NotificationPolicyAbilityParam =
  | { action: NotificationPolicyAction.ViewTree }
  | { action: NotificationPolicyAction.ViewAutogenerated }
  | { action: NotificationPolicyAction.Create }
  | { action: NotificationPolicyAction.UpdateTree; context: NotificationPolicyContext }
  | { action: NotificationPolicyAction.Delete; context: NotificationPolicyContext }
  | { action: NotificationPolicyAction.Export; context: NotificationPolicyContext };

const PERMISSIONS: Record<NotificationPolicyAction, AccessControlAction[]> = {
  [NotificationPolicyAction.ViewTree]: [
    notificationsPermissions.read.grafana,
    AccessControlAction.AlertingRoutesRead,
    AccessControlAction.ActionAlertingManagedRoutesRead,
  ],
  [NotificationPolicyAction.Create]: [
    notificationsPermissions.create.grafana,
    AccessControlAction.AlertingRoutesWrite,
    AccessControlAction.ActionAlertingManagedRoutesCreate,
    AccessControlAction.ActionAlertingManagedRoutesWrite,
    AccessControlAction.ActionAlertingManagedRoutesDelete,
  ],
  [NotificationPolicyAction.UpdateTree]: [
    notificationsPermissions.update.grafana,
    AccessControlAction.AlertingRoutesWrite,
    AccessControlAction.ActionAlertingManagedRoutesCreate,
    AccessControlAction.ActionAlertingManagedRoutesWrite,
    AccessControlAction.ActionAlertingManagedRoutesDelete,
  ],
  [NotificationPolicyAction.Delete]: [
    notificationsPermissions.delete.grafana,
    AccessControlAction.AlertingRoutesWrite,
    AccessControlAction.ActionAlertingManagedRoutesCreate,
    AccessControlAction.ActionAlertingManagedRoutesWrite,
    AccessControlAction.ActionAlertingManagedRoutesDelete,
  ],
  [NotificationPolicyAction.Export]: [notificationsPermissions.read.grafana],
  [NotificationPolicyAction.ViewAutogenerated]: [], // gated by isAdmin(), not by RBAC permissions
};

/** All permissions that gate notification policy functionality — used by datasource access-control checks. */
export const PERMISSIONS_NOTIFICATION_POLICIES: AccessControlAction[] = Object.values(PERMISSIONS).flatMap(
  (permissions) => permissions
);

export function useNotificationPolicyAbility(payload: NotificationPolicyAbilityParam): Ability {
  switch (payload.action) {
    case NotificationPolicyAction.ViewTree:
    case NotificationPolicyAction.Create:
      return makeAbility(true, PERMISSIONS[payload.action]);

    // The autogenerated policy tree is only visible to org admins — it has no RBAC permission
    // of its own. This matches the pre-refactor behaviour where admins got Granted and
    // non-admins got InsufficientPermissions([]).
    case NotificationPolicyAction.ViewAutogenerated:
      return isAdmin() ? Granted : InsufficientPermissions([]);

    case NotificationPolicyAction.UpdateTree:
    case NotificationPolicyAction.Delete:
    case NotificationPolicyAction.Export: {
      if (isProvisionedResource(payload.context.provenance)) {
        return Provisioned;
      }
      return makeAbility(true, PERMISSIONS[payload.action]);
    }
  }
}
