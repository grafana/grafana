import { useMemo } from 'react';

import { contextSrv as ctx } from 'app/core/services/context_srv';
import { PERMISSIONS_CONTACT_POINTS_READ } from 'app/features/alerting/unified/components/contact-points/permissions';
import {
  PERMISSIONS_TIME_INTERVALS_MODIFY,
  PERMISSIONS_TIME_INTERVALS_READ,
} from 'app/features/alerting/unified/components/mute-timings/permissions';
import {
  PERMISSIONS_NOTIFICATION_POLICIES_MODIFY,
  PERMISSIONS_NOTIFICATION_POLICIES_READ,
} from 'app/features/alerting/unified/components/notification-policies/permissions';
import { AccessControlAction } from 'app/types/accessControl';

import { useAlertmanager } from '../state/AlertmanagerContext';
import { getInstancesPermissions, getNotificationsPermissions } from '../utils/access-control';
import { isAdmin } from '../utils/misc';

import {
  type AbilityState,
  type AbilityStates,
  AlertmanagerAction,
  ExternalAlertmanagerAction,
} from './useAbilities.types';

// ── Alertmanager abilities ────────────────────────────────────────────────────

export function useAllAlertmanagerAbilityStates(): AbilityStates<AlertmanagerAction> {
  const {
    selectedAlertmanager,
    hasConfigurationAPI,
    isGrafanaAlertmanager: isGrafanaFlavoredAlertmanager,
  } = useAlertmanager();

  // These are used for interacting with Alertmanager resources where we apply
  // alert.notifications:<name> permissions. Different permissions apply depending
  // on whether the built-in alertmanager is selected (grafana) or an external one.
  const notificationsPermissions = getNotificationsPermissions(selectedAlertmanager!);
  const instancePermissions = getInstancesPermissions(selectedAlertmanager!);
  const admin = isAdmin();

  // Memoize on the three values that determine all permission checks so that
  // downstream useMemo([all, action]) calls aren't defeated by a new object
  // reference on every render.
  return useMemo<AbilityStates<AlertmanagerAction>>(() => {
    // Re-evaluate toAbilityState inside the memo so results are consistent.
    const to = (supported: boolean, ...actions: AccessControlAction[]): AbilityState => {
      const allowed = actions.some((action) => action && ctx.hasPermission(action));
      return { supported, allowed, loading: false, granted: supported && allowed };
    };

    const exportContact: AbilityState = {
      supported: isGrafanaFlavoredAlertmanager,
      allowed: isGrafanaFlavoredAlertmanager,
      loading: false,
      granted: isGrafanaFlavoredAlertmanager,
    };

    const autogenerated: AbilityState = {
      supported: isGrafanaFlavoredAlertmanager,
      allowed: admin,
      loading: false,
      granted: isGrafanaFlavoredAlertmanager && admin,
    };

    return {
      // -- contact points --
      [AlertmanagerAction.CreateContactPoint]: to(
        hasConfigurationAPI,
        notificationsPermissions.create,
        // TODO: Move this into the permissions config and generalise that code to allow for an array of permissions
        ...(isGrafanaFlavoredAlertmanager ? [AccessControlAction.AlertingReceiversCreate] : [])
      ),
      [AlertmanagerAction.ViewContactPoint]: to(
        true,
        notificationsPermissions.read,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_CONTACT_POINTS_READ : [])
      ),
      [AlertmanagerAction.UpdateContactPoint]: to(
        hasConfigurationAPI,
        notificationsPermissions.update,
        ...(isGrafanaFlavoredAlertmanager ? [AccessControlAction.AlertingReceiversWrite] : [])
      ),
      [AlertmanagerAction.DeleteContactPoint]: to(
        hasConfigurationAPI,
        notificationsPermissions.delete,
        ...(isGrafanaFlavoredAlertmanager ? [AccessControlAction.AlertingReceiversWrite] : [])
      ),
      // At the time of writing, only Grafana flavored alertmanager supports exporting,
      // and if a user can view the contact point, then they can also export it.
      // So the only check we make is if the alertmanager is Grafana flavored.
      [AlertmanagerAction.ExportContactPoint]: exportContact,
      // -- notification templates --
      [AlertmanagerAction.CreateNotificationTemplate]: to(
        hasConfigurationAPI,
        notificationsPermissions.create,
        ...(isGrafanaFlavoredAlertmanager ? [AccessControlAction.AlertingTemplatesWrite] : [])
      ),
      [AlertmanagerAction.ViewNotificationTemplate]: to(
        true,
        notificationsPermissions.read,
        ...(isGrafanaFlavoredAlertmanager ? [AccessControlAction.AlertingTemplatesRead] : [])
      ),
      [AlertmanagerAction.UpdateNotificationTemplate]: to(
        hasConfigurationAPI,
        notificationsPermissions.update,
        ...(isGrafanaFlavoredAlertmanager ? [AccessControlAction.AlertingTemplatesWrite] : [])
      ),
      [AlertmanagerAction.DeleteNotificationTemplate]: to(hasConfigurationAPI, notificationsPermissions.delete),
      // -- notification policies --
      [AlertmanagerAction.CreateNotificationPolicy]: to(
        hasConfigurationAPI,
        notificationsPermissions.create,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_NOTIFICATION_POLICIES_MODIFY : [])
      ),
      [AlertmanagerAction.ViewNotificationPolicyTree]: to(
        true,
        notificationsPermissions.read,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_NOTIFICATION_POLICIES_READ : [])
      ),
      [AlertmanagerAction.UpdateNotificationPolicyTree]: to(
        hasConfigurationAPI,
        notificationsPermissions.update,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_NOTIFICATION_POLICIES_MODIFY : [])
      ),
      [AlertmanagerAction.DeleteNotificationPolicy]: to(
        hasConfigurationAPI,
        notificationsPermissions.delete,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_NOTIFICATION_POLICIES_MODIFY : [])
      ),
      [AlertmanagerAction.ExportNotificationPolicies]: to(isGrafanaFlavoredAlertmanager, notificationsPermissions.read),
      [AlertmanagerAction.DecryptSecrets]: to(
        isGrafanaFlavoredAlertmanager,
        notificationsPermissions.provisioning.readSecrets
      ),
      [AlertmanagerAction.ViewAutogeneratedPolicyTree]: autogenerated,
      // -- silences --
      // All supported Alertmanager flavors have API endpoints for managing silences
      [AlertmanagerAction.CreateSilence]: to(true, instancePermissions.create),
      [AlertmanagerAction.ViewSilence]: to(true, instancePermissions.read),
      [AlertmanagerAction.UpdateSilence]: to(true, instancePermissions.update),
      [AlertmanagerAction.PreviewSilencedInstances]: to(true, instancePermissions.read),
      // -- time intervals --
      [AlertmanagerAction.CreateTimeInterval]: to(
        hasConfigurationAPI,
        notificationsPermissions.create,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_TIME_INTERVALS_MODIFY : [])
      ),
      [AlertmanagerAction.ViewTimeInterval]: to(
        true,
        notificationsPermissions.read,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_TIME_INTERVALS_READ : [])
      ),
      [AlertmanagerAction.UpdateTimeInterval]: to(
        hasConfigurationAPI,
        notificationsPermissions.update,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_TIME_INTERVALS_MODIFY : [])
      ),
      [AlertmanagerAction.DeleteTimeInterval]: to(
        hasConfigurationAPI,
        notificationsPermissions.delete,
        ...(isGrafanaFlavoredAlertmanager ? PERMISSIONS_TIME_INTERVALS_MODIFY : [])
      ),
      [AlertmanagerAction.ExportTimeIntervals]: to(isGrafanaFlavoredAlertmanager, notificationsPermissions.read),
      [AlertmanagerAction.ViewAlertGroups]: to(true, instancePermissions.read),
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [selectedAlertmanager, hasConfigurationAPI, isGrafanaFlavoredAlertmanager, admin]);
}

export function useAlertmanagerAbilityState(action: AlertmanagerAction): AbilityState {
  const all = useAllAlertmanagerAbilityStates();
  return useMemo(() => all[action], [all, action]);
}

export function useAlertmanagerAbilityStates(actions: AlertmanagerAction[]): AbilityState[] {
  const all = useAllAlertmanagerAbilityStates();
  return useMemo(() => actions.map((action) => all[action]), [all, actions]);
}

// ── External alertmanager abilities ──────────────────────────────────────────

export function useAllExternalAlertmanagerAbilityStates(): AbilityStates<ExternalAlertmanagerAction> {
  const { hasConfigurationAPI } = useAlertmanager();

  return useMemo<AbilityStates<ExternalAlertmanagerAction>>(() => {
    const to = (supported: boolean, ...actions: AccessControlAction[]): AbilityState => {
      const allowed = actions.some((action) => action && ctx.hasPermission(action));
      return { supported, allowed, loading: false, granted: supported && allowed };
    };
    return {
      [ExternalAlertmanagerAction.ViewExternalConfiguration]: to(
        true,
        AccessControlAction.AlertingNotificationsExternalRead
      ),
      [ExternalAlertmanagerAction.UpdateExternalConfiguration]: to(
        hasConfigurationAPI,
        AccessControlAction.AlertingNotificationsExternalWrite
      ),
    };
  }, [hasConfigurationAPI]);
}

export function useExternalAlertmanagerAbilityState(action: ExternalAlertmanagerAction): AbilityState {
  const all = useAllExternalAlertmanagerAbilityStates();
  return useMemo(() => all[action], [all, action]);
}
