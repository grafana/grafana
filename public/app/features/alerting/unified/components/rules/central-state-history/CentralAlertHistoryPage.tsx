import { css } from '@emotion/css';

import { GrafanaTheme2 } from '@grafana/data';
import { config } from '@grafana/runtime';
import { Tab, TabContent, TabsBar, Text, useStyles2 } from '@grafana/ui';
import { useQueryParams } from 'app/core/hooks/useQueryParams';

import { NotificationsScene } from '../../../notifications/NotificationsScene';
import { withPageErrorBoundary } from '../../../withPageErrorBoundary';
import { AlertingPageWrapper } from '../../AlertingPageWrapper';

import { CentralAlertHistoryScene } from './CentralAlertHistoryScene';

enum ActiveTab {
  AlertEvents = 'alert_events',
  Notifications = 'notifications',
}

function HistoryPage() {
  const styles = useStyles2(getStyles);
  const [queryParams, setQueryParams] = useQueryParams();
  const activeTab =
    queryParams.tab === ActiveTab.Notifications ? ActiveTab.Notifications : ActiveTab.AlertEvents;

  const notificationsEnabled = config.featureToggles.alertingNotificationHistoryGlobal;

  return (
    <AlertingPageWrapper navId="alerts-history" isLoading={false}>
      {notificationsEnabled && (
        <TabsBar>
          <Tab
            label="Alert events"
            active={activeTab === ActiveTab.AlertEvents}
            onChangeTab={() => setQueryParams({ tab: ActiveTab.AlertEvents })}
          />
          <Tab
            label="Notifications"
            active={activeTab === ActiveTab.Notifications}
            onChangeTab={() => setQueryParams({ tab: ActiveTab.Notifications })}
          />
        </TabsBar>
      )}
      <TabContent className={styles.tabContent}>
        <div className={styles.description}>
          <Text color="secondary" variant="body">
            {activeTab === ActiveTab.AlertEvents
              ? 'View a history of all alert events generated by your Grafana-managed alert rules. All alert events are displayed regardless of whether silences or mute timings are set.'
              : 'View a history of all notifications sent from your Grafana-managed alert rules.'}
          </Text>
        </div>
        {activeTab === ActiveTab.AlertEvents && <CentralAlertHistoryScene />}
        {activeTab === ActiveTab.Notifications && notificationsEnabled && <NotificationsScene />}
      </TabContent>
    </AlertingPageWrapper>
  );
}

const getStyles = (theme: GrafanaTheme2) => ({
  tabContent: css({
    marginTop: theme.spacing(2),
  }),
  description: css({
    paddingBottom: theme.spacing(2),
  }),
});

export default withPageErrorBoundary(HistoryPage);
