package imguploader

import (
	"context"
	"fmt"
	"os"
	"time"

	"github.com/Azure/azure-sdk-for-go/sdk/storage/azblob"
	"github.com/Azure/azure-sdk-for-go/sdk/storage/azblob/blob"
	"github.com/Azure/azure-sdk-for-go/sdk/storage/azblob/sas"

	"github.com/grafana/grafana/pkg/infra/log"
	"github.com/grafana/grafana/pkg/util"
)

type AzureBlobUploader struct {
	accountName            string
	accountKey             string
	containerName          string
	sasTokenExpirationDays int
	log                    log.Logger
}

func NewAzureBlobUploader(accountName string, accountKey string, containerName string, sasTokenExpirationDays int) *AzureBlobUploader {
	return &AzureBlobUploader{
		accountName:            accountName,
		accountKey:             accountKey,
		containerName:          containerName,
		sasTokenExpirationDays: sasTokenExpirationDays,
		log:                    log.New("azureBlobUploader"),
	}
}

// Receive path of image on disk and return azure blob url
func (az *AzureBlobUploader) Upload(ctx context.Context, imageDiskPath string) (string, error) {
	cred, err := azblob.NewSharedKeyCredential(az.accountName, az.accountKey)
	if err != nil {
		return "", fmt.Errorf("creating shared key credential: %w", err)
	}

	serviceURL := fmt.Sprintf("https://%s.blob.core.windows.net", az.accountName)

	client, err := azblob.NewClientWithSharedKeyCredential(serviceURL, cred, nil)
	if err != nil {
		return "", fmt.Errorf("creating azure blob client: %w", err)
	}

	// We can ignore the gosec G304 warning on this one because `imageDiskPath` comes
	// from alert notifiers and is only used to upload images generated by alerting.
	// nolint:gosec
	file, err := os.Open(imageDiskPath)
	if err != nil {
		return "", err
	}
	defer func() {
		if err := file.Close(); err != nil {
			az.log.Warn("Failed to close file", "path", imageDiskPath, "err", err)
		}
	}()

	randomFileName, err := util.GetRandomString(30)
	if err != nil {
		return "", err
	}

	randomFileName += pngExt

	az.log.Debug("Uploading image to azure_blob", "container_name", az.containerName, "blob_name", randomFileName)

	contentType := "image/png"
	if _, err := client.UploadFile(ctx, az.containerName, randomFileName, file, &azblob.UploadFileOptions{
		HTTPHeaders: &blob.HTTPHeaders{
			BlobContentType: &contentType,
		},
	}); err != nil {
		return "", fmt.Errorf("uploading to azure blob: %w", err)
	}

	blobURL := fmt.Sprintf("https://%s.blob.core.windows.net/%s/%s", az.accountName, az.containerName, randomFileName)

	if az.sasTokenExpirationDays > 0 {
		sasQueryParams, err := sas.BlobSignatureValues{
			Protocol:      sas.ProtocolHTTPS,
			ExpiryTime:    time.Now().UTC().AddDate(0, 0, az.sasTokenExpirationDays),
			ContainerName: az.containerName,
			BlobName:      randomFileName,
			Permissions:   (&sas.BlobPermissions{Read: true}).String(),
		}.SignWithSharedKey(cred)
		if err != nil {
			return "", fmt.Errorf("signing SAS token: %w", err)
		}

		return fmt.Sprintf("%s?%s", blobURL, sasQueryParams.Encode()), nil
	}

	return blobURL, nil
}
