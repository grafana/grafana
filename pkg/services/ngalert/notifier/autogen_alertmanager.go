type autogenRuleStore interface {
	ListContactPointRoutings(ctx context.Context, q models.ListContactPointRoutingsQuery) (map[models.AlertRuleKey]models.ContactPointRouting, error)
}

type InvalidReceiversAction string

const (
	ErrorOnInvalidReceivers InvalidReceiversAction = "error"
	LogInvalidReceivers     InvalidReceiversAction = "log"
	IgnoreInvalidReceivers  InvalidReceiversAction = "ignore"
)

// AddAutogenConfig creates the autogenerated configuration and adds it to the given apiAlertingConfig.
// If skipInvalid is true, then invalid notification settings are skipped, otherwise an error is returned.
func AddAutogenConfig(ctx context.Context, logger log.Logger, store autogenRuleStore, orgId int64, cfg *v1.PostableApiAlertingConfig, invalidReceiverAction InvalidReceiversAction, features featuremgmt.FeatureToggles, timings *autogenRouteTimings) error {
	autogenRoute, err := newAutogeneratedRoute(ctx, logger, store, orgId, cfg, invalidReceiverAction, features, timings)
	if err != nil {
		return err
	}

	err = autogenRoute.addToRoute(cfg.GetRoute())
	if err != nil {
		return err
	}

	return nil
}

// newAutogeneratedRoute creates a new autogenerated route based on the notification settings for the given org.
// cfg is used to construct the settings validator and to ensure we create a dedicated route for each receiver.
// skipInvalid is used to skip invalid settings instead of returning an error.
func newAutogeneratedRoute(ctx context.Context, logger log.Logger, store autogenRuleStore, orgId int64, cfg *v1.PostableApiAlertingConfig, invalidReceiverAction InvalidReceiversAction, features featuremgmt.FeatureToggles, timings *autogenRouteTimings) (autogeneratedRoute, error) {
	settings, err := store.ListContactPointRoutings(ctx, models.ListContactPointRoutingsQuery{OrgID: orgId})
	if err != nil {
		return autogeneratedRoute{}, fmt.Errorf("failed to list alert rules: %w", err)
	}

	notificationSettings := make(map[data.Fingerprint]models.ContactPointRouting)
	// Add a default notification setting for each contact point. This is to ensure that we always have a route for each
	// contact point even if no rules are using it. This will prevent race conditions between AM sync and rule sync.
	for _, receiver := range cfg.GetReceivers() {
		setting := models.NewDefaultContactPointRouting(receiver.GetName())
		fp := setting.Fingerprint(features)
		notificationSettings[fp] = setting
	}

	validator := NewContactPointRoutingValidator(cfg)
	for ruleKey, setting := range settings {
		// TODO we should register this errors and somehow present to the users or make sure the config is always valid.
		if err = validator.Validate(setting); err != nil {
			switch invalidReceiverAction {
			case ErrorOnInvalidReceivers:
				return autogeneratedRoute{}, fmt.Errorf("invalid notification settings for rule %s: %w", ruleKey.UID, err)
			case LogInvalidReceivers:
				logger.Warn("Rule notification settings are invalid. Skipping", append(ruleKey.LogContext(), "error", err)...)
			case IgnoreInvalidReceivers: // do nothing
			}
			continue
		}
		fp := setting.Fingerprint(features)
		// Keep only unique settings.
		if _, ok := notificationSettings[fp]; ok {
			continue
		}
		notificationSettings[fp] = setting
	}
	if len(notificationSettings) == 0 {
		return autogeneratedRoute{}, nil
	}
	newAutogenRoute, err := generateRouteFromSettings(cfg.GetRoute().Receiver, notificationSettings, timings)
	if err != nil {
		return autogeneratedRoute{}, fmt.Errorf("failed to create autogenerated route: %w", err)
	}
	return newAutogenRoute, nil
}

type autogeneratedRoute struct {
	Route *v1.Route
}

// generateRouteFromSettings generates a route and fingerprint for this route. The route is a tree of 3 layers:
//  1. with matcher by label models.AutogeneratedRouteLabel equals 'true'.
//  2. with matcher by receiver name.
//  3. with matcher by unique combination of optional settings. It is created only if there are optional settings.
//
// The root of the autogenerated policy (layer 1) is always given explicit
// group_wait/group_interval/repeat_interval values so it never inherits timings
// from the user's root route: each field is taken from timings when set, else
// from the Alertmanager route defaults (dispatch.DefaultRouteOpts). This mirrors
// how managed routes are fully defined in legacy_storage.GeneratedSubRoute.
func generateRouteFromSettings(defaultReceiver string, settings map[data.Fingerprint]models.ContactPointRouting, timings *autogenRouteTimings) (autogeneratedRoute, error) {
	// sort keys to make sure that the hash we calculate using it is stable
	keys := slices.Sorted(maps.Keys(settings))

	rootMatcher, err := labels.NewMatcher(labels.MatchEqual, models.AutogeneratedRouteLabel, "true")
	if err != nil {
		return autogeneratedRoute{}, err
	}

	groupWait, groupInterval, repeatInterval := resolveAutogenRootTimings(timings)
	autoGenRoot := &v1.Route{
		Receiver:       defaultReceiver,
		ObjectMatchers: v1.ObjectMatchers{rootMatcher},
		Continue:       false, // We explicitly don't continue toward user-created routes if this matches.

		// Fully define the timings so the autogen root never inherits them from
		// the user's root route.
		GroupWait:      groupWait,
		GroupInterval:  groupInterval,
		RepeatInterval: repeatInterval,
	}

	receiverRoutes := make(map[string]*v1.Route)
	for _, fingerprint := range keys {
		s := settings[fingerprint]
		receiverRoute, ok := receiverRoutes[s.Receiver]
		if !ok {
			contactMatcher, err := labels.NewMatcher(labels.MatchEqual, models.AutogeneratedRouteReceiverNameLabel, s.Receiver)
			if err != nil {
				return autogeneratedRoute{}, err
			}
			groupByStr := append([]string{}, models.DefaultNotificationSettingsGroupBy...)
			groupByAll, groupBy := legacy_storage.ToGroupBy(groupByStr...)
			receiverRoute = &v1.Route{
				Receiver:       s.Receiver,
				ObjectMatchers: v1.ObjectMatchers{contactMatcher},
				Continue:       false,
				// Since we'll have many rules from different folders using this policy, we ensure it has these necessary groupings.
				GroupByStr: groupByStr,
				GroupBy:    groupBy,
				GroupByAll: groupByAll,
			}
			receiverRoutes[s.Receiver] = receiverRoute
			autoGenRoot.Routes = append(autoGenRoot.Routes, receiverRoute)
		}

		// Do not create hash specific route if all group settings such as mute timings, group_wait, group_interval, etc are default
		if s.IsAllDefault() {
			continue
		}
		settingMatcher, err := labels.NewMatcher(labels.MatchEqual, models.AutogeneratedRouteSettingsHashLabel, fingerprint.String())
		if err != nil {
			return autogeneratedRoute{}, err
		}
		normalized := s.NormalizedGroupBy()
		groupByAll, groupBy := legacy_storage.ToGroupBy(normalized...)
		receiverRoute.Routes = append(receiverRoute.Routes, &v1.Route{
			Receiver:       s.Receiver,
			ObjectMatchers: v1.ObjectMatchers{settingMatcher},
			Continue:       false, // Only a single setting-specific route should match.

			GroupByStr:          normalized,
			GroupBy:             groupBy,
			GroupByAll:          groupByAll,
			MuteTimeIntervals:   s.MuteTimeIntervals,
			ActiveTimeIntervals: s.ActiveTimeIntervals,
			GroupWait:           s.GroupWait,
			GroupInterval:       s.GroupInterval,
			RepeatInterval:      s.RepeatInterval,
		})
	}

	return autogeneratedRoute{
		Route: autoGenRoot,
	}, nil
}

// resolveAutogenRootTimings returns the group_wait/group_interval/repeat_interval
// to set explicitly on the autogen root. Each value comes from timings when set,
// otherwise from the Alertmanager route defaults (dispatch.DefaultRouteOpts), so
// the returned pointers are always non-nil and the autogen root is fully defined.
func resolveAutogenRootTimings(timings *autogenRouteTimings) (groupWait, groupInterval, repeatInterval *model.Duration) {
	defaultOpts := dispatch.DefaultRouteOpts
	gw := model.Duration(defaultOpts.GroupWait)
	gi := model.Duration(defaultOpts.GroupInterval)
	ri := model.Duration(defaultOpts.RepeatInterval)
	if timings != nil {
		if timings.GroupWait != nil {
			gw = *timings.GroupWait
		}
		if timings.GroupInterval != nil {
			gi = *timings.GroupInterval
		}
		if timings.RepeatInterval != nil {
			ri = *timings.RepeatInterval
		}
	}
	return &gw, &gi, &ri
}

// addToRoute adds this autogenerated route to the given route as the first top-level route under the root.
func (ar *autogeneratedRoute) addToRoute(route *v1.Route) error {
	if route == nil {
		return errors.New("route does not exist")
	}
	if ar == nil || ar.Route == nil {
		return nil
	}
	// Combine autogenerated route with the user-created route.
	ar.Route.Receiver = route.Receiver

	// Remove existing autogenerated route if it exists.
	RemoveAutogenConfigIfExists(route)

	route.Routes = append([]*v1.Route{ar.Route}, route.Routes...)
	return nil
}

// RemoveAutogenConfigIfExists removes all top-level autogenerated routes from the provided route.
// If no autogenerated routes exist, this function does nothing.
func RemoveAutogenConfigIfExists(route *v1.Route) {
	route.Routes = slices.DeleteFunc(route.Routes, func(route *v1.Route) bool {
		return IsAutogeneratedRoot(route)
	})
}

// IsAutogeneratedRoot returns true if the route is the root of an autogenerated route.
func IsAutogeneratedRoot(route *v1.Route) bool {
	return len(route.ObjectMatchers) == 1 && route.ObjectMatchers[0].Name == models.AutogeneratedRouteLabel
}
