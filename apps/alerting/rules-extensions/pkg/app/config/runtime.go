package config

import (
	"context"
	"errors"
)

var (
	ErrInvalidRuntimeConfig = errors.New("invalid runtime config provided to alerting/rules-extensions app")
)

// FallbackDatasourceUID is used as the last-resort datasource UID when neither the file's spec
// nor the runtime config supplies one. It points at the default Grafana Cloud Prometheus
// datasource, which is the expected target for Prometheus-style rules in a Grafana Cloud stack.
const FallbackDatasourceUID = "grafanacloud-prom"

// RuntimeConfig holds configuration values needed at runtime by the alerting/rules-extensions app.
// The configuration is supplied by the running Grafana instance when it registers the app installer.
type RuntimeConfig struct {
	// FolderValidator checks that the given folder UID exists and is visible to the caller.
	// It is used by the validator to ensure the parent folder annotation refers to a real folder.
	// When nil, no folder existence check is performed.
	FolderValidator func(ctx context.Context, folderUID string) (bool, error)

	// DefaultDatasourceUID is the datasource UID assigned to the AlertRule / RecordingRule
	// expressions generated by the reconciler when the PrometheusRuleFile spec does not pin
	// one. If empty, FallbackDatasourceUID is used.
	DefaultDatasourceUID string

	// DatasourceUIDResolver, when set, is invoked for every rule entry to resolve its
	// datasource UID at reconciliation time (e.g. by looking up the default Prometheus
	// datasource in the calling org). Returning ("", nil) signals "use DefaultDatasourceUID".
	// When nil, the reconciler falls back to DefaultDatasourceUID for every rule.
	DatasourceUIDResolver func(ctx context.Context) (string, error)
}

// ResolveDatasourceUID returns the datasource UID to use for a rule, applying the configured
// resolver first and falling back to the static default. It never returns the empty string —
// FallbackDatasourceUID is used as a last resort.
func (c RuntimeConfig) ResolveDatasourceUID(ctx context.Context) (string, error) {
	if c.DatasourceUIDResolver != nil {
		uid, err := c.DatasourceUIDResolver(ctx)
		if err != nil {
			return "", err
		}
		if uid != "" {
			return uid, nil
		}
	}
	if c.DefaultDatasourceUID != "" {
		return c.DefaultDatasourceUID, nil
	}
	return FallbackDatasourceUID, nil
}
